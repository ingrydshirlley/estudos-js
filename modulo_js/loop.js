/*LOOP FOR
    for (inicializador; condição; expressão) {
        código que será executado
    }
*/

for (let i = 0; i < 5; i++) { //enquanto i for menor q 5, acrescente mais 1
    console.log(i) //saída: 0, 1, 2, 3, 4.
}


/*LOOP WHILE

    inicializador
    while (condição) {
        código 
        expressão
    }

*/

var i = 0
while (i < 5) {
    console.log(i)
    i++
}